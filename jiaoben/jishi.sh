#!/bin/bash
a=( 00000   "  1  "  22222   33333  "4 4  "  55555   66666   77777   88888   99999 )
b=("0   0"  " 11  " "    2" "    3" "4 4  " "5    " "6    " "    7" "8   8" "9   9")
c=("0   0"  "  1  "  22222   33333   44444   55555   66666  "    7"  88888   99999 )
d=("0   0"  "  1  " "2    " "    3" "  4  " "    5" "6   6" "    7" "8   8" "    9")
e=( 00000    11111   22222   33333  "  4  "  55555   66666  "    7"  88888   99999 )

read -p "请输入时间s:" w
x=$[w%100%10]
y=$[w%100/10]
z=$[w/100]
while :
do
echo -e "\033[12;30H\033[31m${a[x]}\033[0m"
echo -e "\033[13;30H\033[31m${b[x]}\033[0m"
echo -e "\033[14;30H\033[31m${c[x]}\033[0m"
echo -e "\033[15;30H\033[31m${d[x]}\033[0m"
echo -e "\033[16;30H\033[31m${e[x]}\033[0m"

echo -e "\033[12;23H\033[31m${a[y]}\033[0m"
echo -e "\033[13;23H\033[31m${b[y]}\033[0m"
echo -e "\033[14;23H\033[31m${c[y]}\033[0m"
echo -e "\033[15;23H\033[31m${d[y]}\033[0m"
echo -e "\033[16;23H\033[31m${e[y]}\033[0m"

echo -e "\033[12;16H\033[31m${a[z]}\033[0m"
echo -e "\033[13;16H\033[31m${b[z]}\033[0m"
echo -e "\033[14;16H\033[31m${c[z]}\033[0m"
echo -e "\033[15;16H\033[31m${d[z]}\033[0m"
echo -e "\033[16;16H\033[31m${e[z]}\033[0m"
sleep 1
let x--
[ $x -lt 0 ] &&  x=9 && let y-- 
[ $y -lt 0 ] &&  y=9 && let z--
[ $z -lt 0 ] && break
done
